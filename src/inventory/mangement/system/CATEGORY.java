    private void DeleteBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_DeleteBtnMouseClicked
         // to check if the text field of Categoryid or Categorynameis empty or not if empty show this message else excuete this code
        if(CateId.getText().isEmpty()||CateName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else{
        try {
            //to make connection between database inventorydb and program 
            Con =DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb", "root", "S##48k@qirt");
            //to go to row where Categoryid of this product is exsist to delete because Categoryid is primary key
            String Id=CateId.getText();
            //to  PreparedStatement called Query to delete this values  from CATEGORYTBL table in database inventorydb
            String Query ="Delete from CATEGORYTBL where CATEID="+Id;
            //to create statement in database inventorydb
            Statement Add=Con.createStatement();
            // to execute this statement in CATEGORYTBL table in database inventorydb
            Add.executeUpdate(Query);
            // to show values in the table
            SelectCate();
            //this message will show when you deleted a values from CATEGORYTBL table in database inventorydb
            JOptionPane.showMessageDialog(this,"Category deleted Successfully");
            //to clear after deleted
            clear();
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    }//GEN-LAST:event_DeleteBtnMouseClicked

