    private void AddBtnMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_AddBtnMouseClicked
          // to check if the text field of categoryid or Categoryname is empty or not if empty show this message else excuete this code
        if(CateId.getText().isEmpty()||CateName.getText().isEmpty()){
            JOptionPane.showMessageDialog(this,"Missing Information");
        }
        else{
        try {
            //to make connection between database inventorydb and program 
            Con =DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb", "root", "S##48k@qirt");
            //to  PreparedStatement called add to insert this values into CATEGORYTBL table in database inventorydb
            PreparedStatement add = Con.prepareStatement("insert into CATEGORYTBL values(?,?)");
            //to enter values in CATEGORYTBL table in database inventorydb
            add.setInt(1,Integer.valueOf(CateId.getText()));
            add.setString(2,CateName.getText());
              // to execute this statement in CATEGORYTBLtable in database inventorydb
            int row=add.executeUpdate();
            //this message will show when you added a values to CATEGORYTBL table table in database inventorydb
            JOptionPane.showMessageDialog(this,"Category Successfully Added");
            // to close connection
            Con.close();
            //to show values in the table
            SelectCate();
             //to clear after added
            clear();
        } catch (SQLException e) {
            e.printStackTrace();
        }
         }
    }//GEN-LAST:event_AddBtnMouseClicked
