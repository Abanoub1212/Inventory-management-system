    private void loginMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_loginMouseClicked
       //to check if the username or password is empty or not if empty show this message else execute the next code  
        if(Userlog.getText().isEmpty()||Passwordlog.getText().isEmpty())
       {
           JOptionPane.showMessageDialog(this, "Missing Information");
       }
       else{
            // to enter to database inventorydb in USERTBL table and check if this username and password is exsist or not
         String Query = "select * from USERTBL where USERNAME='"+Userlog.getText()+"'and USERPASSWORD='"+Passwordlog.getText()+"'";
        try {
            //to make connection between database inventorydb and program 
            Con =DriverManager.getConnection("jdbc:mysql://localhost:3306/inventorydb", "root", "S##48k@qirt");
            //to create statement in database inventorydb
         St=Con.createStatement();
         // to execute this statemnt in database inventorydb
         Rs=St.executeQuery(Query);
         //to check if it is exsist go to Home Page else show this message
            if (Rs.next()) {
                new HomeForm().setVisible(true);
                this.dispose();
            }
            else{
                JOptionPane.showMessageDialog(this, "Wrong UserName Or Password");
            }
        } catch (Exception e) {
        e.printStackTrace();
        }
        }
    }//GEN-LAST:event_loginMouseClicked

